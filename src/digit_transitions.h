// -1: no matching transition
// 0-8: transitions from i to i+1
// 9-12: transitions from 0 to 2,3,5,9
// 13: transition from 1 to 9
int get_digit_transition(int lower, int higher) {
	if (higher != lower+1) { // no basic i to i+1 transition
		if (lower == 0) { // from 0 to 2,3,5,9
			switch (higher) {
				case 2: return 9;
				case 3: return 10;
				case 5: return 11;
				case 9: return 12;
				default: return -1; // invalid transition
			}
		} else if (lower == 1 && higher == 9) { // from 1 to 9
			return 13;
		}
		return -1; // invalid transition
	}
	return lower; // i to i+1 transition
}

